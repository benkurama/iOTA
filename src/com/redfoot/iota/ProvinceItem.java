package com.redfoot.iota;

public class ProvinceItem {
	
	private CodeDescPair m_cdpItem;
	private boolean m_bSelected;
	
	public CodeDescPair getItem() { return m_cdpItem; }
	public void setItem(CodeDescPair cdp) { m_cdpItem = cdp; }
	public boolean isSelected() { return m_bSelected; }
	public void setSelected(boolean bool) { m_bSelected = bool; }
	
	public ProvinceItem(CodeDescPair cdp) {
		m_cdpItem = cdp;
		m_bSelected = false;
	}
}
